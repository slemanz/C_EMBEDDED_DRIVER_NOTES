/*********************************************************************
 * @fn      		  - I2C_DeInit
 *
 * @brief             -
 *
 * @param[in]         -
 * @param[in]         -
 * @param[in]         -
 *
 * @return            -
 *
 * @Note              -

 */

void I2C_DeInit(I2C_RegDef_t *pI2Cx)
{
	if		(pI2Cx == I2C1) I2C1_REG_RESET();
	else if	(pI2Cx == I2C2) I2C2_REG_RESET();
	else if	(pI2Cx == I2C3) I2C3_REG_RESET();
}



























